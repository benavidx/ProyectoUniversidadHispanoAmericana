    public void getEdad(){
     try {
            if (ClienteFechaNacimiento.getText().length() == 10) {
                java.util.Date fechaActual = new java.util.Date();
                java.text.SimpleDateFormat formato = new java.text.SimpleDateFormat("dd-MM-yyyy");
                String hoy = formato.format(fechaActual);
                String[] dat1 = ClienteFechaNacimiento.getText().split("-");
                String[] dat2 = hoy.split("-");
                int anos = Integer.parseInt(dat2[2]) - Integer.parseInt(dat1[2]);
                int mes = Integer.parseInt(dat2[1]) - Integer.parseInt(dat1[1]);
                if (mes < 0) {
                    anos = anos - 1;
                } else if (mes == 0) {
                    int dia = Integer.parseInt(dat2[0]) - Integer.parseInt(dat1[0]);
                    if (dia > 0) {
                        anos = anos - 1;
                    }
                }
                if (anos > 18) {
                    ClienteEdad.setText(String.valueOf(anos) + " a√±os. Mayor de edad");
                } else {
                    ClienteEdad.setText(String.valueOf(anos) + " a√±os. Menor de edad");
                }
            } else {
                ClienteEdad.setText("Formato no valido");
            }
        } catch (Exception e) {
            ClienteEdad.setText("Formato no valido");
        }
    }
    
